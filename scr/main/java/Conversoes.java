public class Conversoes {

    public static double converterMetrosParaCentimetros(double metros) {
        return metros * 100;
    }

    public static double converterCentimetrosParaMetros(double centimetros) {
        return centimetros / 100;
    }
    public static double converterMetrosParaQuilometros(double metros) {
        return metros / 1000;
    }
    public static double converterQuilometrosParaMetros(double quilometros) {
        return quilometros * 1000;
    }
    public static double converterMetrosParaMilimetros(double metros) {
        return metros / 1000;
    }
    public static double converterMilimetrosParaMetros(double milimetros) {
        return milimetros * 1000;
    }
}
